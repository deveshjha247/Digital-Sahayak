def get_greeting(language: str = "hi") -> str:
    """Get time-appropriate greeting"""
    hour = datetime.now().hour
    
    if 5 <= hour < 12:
        period = "morning"
    elif 12 <= hour < 17:
        period = "afternoon"
    elif 17 <= hour < 21:
        period = "evening"
    else:
        period = "night"
    
    greetings = GREETINGS.get(language, GREETINGS["hi"]).get(period, ["ðŸ™"])
    return random.choice(greetings)


