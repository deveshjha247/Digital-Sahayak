# ===================== HELPER FUNCTIONS =====================

def get_regional_template(region: str, template_type: str, language: str = "hi") -> Optional[str]:
    """Get region-specific template"""
    region = region.lower() if region else "default"
    region_templates = REGIONAL_TEMPLATES.get(region, REGIONAL_TEMPLATES["default"])
    templates = region_templates.get(template_type, [])
    
    if templates:
        return random.choice(templates)
    return None


def get_messaging_template(template_type: str, language: str = "hi") -> Optional[str]:
    """Get messaging-friendly template (works with any chat platform)"""
    templates = MESSAGING_TEMPLATES.get(language, {}).get(template_type, [])
    if templates:
        return random.choice(templates)
    return None


def get_category_template(category: str, language: str = "hi") -> Optional[str]:
    """Get category-specific job template"""
    category = category.lower() if category else "default"
    templates = ENHANCED_JOB_TEMPLATES.get(language, {}).get(category)
    
    if not templates:
        templates = ENHANCED_JOB_TEMPLATES.get(language, {}).get("default", [])
    
    if templates:
        return random.choice(templates)
    return None


def get_state_name(state_code: str, language: str = "hi") -> str:
    """Get state name in specified language"""
    state_code = state_code.lower() if state_code else "all"
    state_info = STATE_NAMES.get(state_code, {"hi": state_code, "en": state_code})
    return state_info.get(language, state_code)


def get_state_code(state_name: str) -> Optional[str]:
    """Get state code from state name"""
    state_name_lower = state_name.lower() if state_name else ""
    
    for code, info in STATE_NAMES.items():
        if (code == state_name_lower or 
            info.get("hi", "").lower() == state_name_lower or
            info.get("en", "").lower() == state_name_lower):
            return code
    return None


def get_all_states(language: str = "hi") -> List[Dict]:
    """Get list of all states"""
    return [
        {
            "code": code,
            "name": info.get(language, info.get("en")),
            "state_code": info.get("code", "")
        }
        for code, info in STATE_NAMES.items()
    ]


def get_conversational_template(template_type: str, language: str = "hi") -> Optional[str]:
    """Get conversational template"""
    templates = CONVERSATIONAL_TEMPLATES.get(language, {}).get(template_type, [])
    if templates:
        return random.choice(templates)
    return None


def get_salary_template(min_salary: int, max_salary: int, language: str = "hi") -> str:
    """Get formatted salary template"""
    templates = SALARY_TEMPLATES.get(language, SALARY_TEMPLATES["hi"])
    template = random.choice(templates)
    return template.format(min_salary=min_salary, max_salary=max_salary)


def get_qualification_text(qual_code: str, language: str = "hi") -> str:
    """Get qualification text from code"""
    qual_code = qual_code.lower() if qual_code else ""
    quals = QUALIFICATION_TEMPLATES.get(language, {}).get(qual_code, [qual_code])
    return random.choice(quals) if quals else qual_code


